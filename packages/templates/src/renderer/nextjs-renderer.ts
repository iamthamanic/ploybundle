export function renderNextjsPackageJson(config: ProjectConfig, _preset: PresetDefinition): string {
  const pkg = {
    name: `@${config.projectName}/app`,
    version: "0.1.0",
    private: true,
    scripts: {
      dev: "next dev",
      build: "next build",
      start: "next start -p 3000",
      lint: "next lint",
    },
    dependencies: {
      next: "^14.2.0",
      react: "^18.3.0",
      "react-dom": "^18.3.0",
    },
    devDependencies: {
      "@types/node": "^20.14.0",
      "@types/react": "^18.3.0",
      "@types/react-dom": "^18.3.0",
      typescript: "^5.5.0",
    },
  };

  return JSON.stringify(pkg, null, 2);
}

